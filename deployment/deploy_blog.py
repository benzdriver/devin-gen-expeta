def run_blog_app():
    """Run the Flask blog application"""
    print("Starting the Flask blog application...")
    
    try:
        from app import app
        
        app.run(debug=True, host='0.0.0.0', port=5000)
    except Exception as e:
        print(f"‚ùå Error starting the Flask application: {str(e)}")
        sys.exit(1)

def main():
    """Main function to deploy the blog"""
    print("\nüöÄ Deploying Expeta-generated Blog Website")
    print("=" * 80)
    
    setup_sample_data()
    
    create_missing_templates()
    
    print("\n‚úÖ Blog deployment complete!")
    print("=" * 80)
    print("To view the blog, open your browser and navigate to: http://localhost:5000")
    print("Press Ctrl+C to stop the server when done.")
    print("=" * 80)
    
    run_blog_app()

if __name__ == "__main__":
    main()
